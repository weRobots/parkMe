package we.robots.parkme.park;

public enum Direction {
	// front, but cover only the left
	FRONT_LEFT,

	// front, but cover only the right
	FRONT_RIGHT,

	// back, but cover only the left
	BACK_LEFT,

	// back, but cover only the right
	BACK_RIGHT,

	// upper left slot
	LEFT_FRONT,

	// lower left slot
	LEFT_BACK,

	// upper right slot
	RIGHT_FRONT,

	// lower right slot
	RIGHT_BACK,
}
